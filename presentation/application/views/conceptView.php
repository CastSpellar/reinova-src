        </p>
        
         <p style="color: #FFF">
            Conscientes dos factos enunciados anteriormente, bem como da crescente problemática ambiental, consideramos que o mau aproveitamento de recursos patente nos dias de hoje, constitui um verdadeiro problema
            que pode e deve ser tratado de forma bem mais eficaz.                       
        </p>
        
        <p style="color: #FFF">
           Desta forma, o nosso conceito/ideia, consiste numa melhor logística do reaproveitamento de resíduos industriais, mais precisamente dos resíduos produzidos pela indústria transformadora e extractiva, uma vez que estas são as principais actividades responsáveis pela acumulação de resíduos.                    
