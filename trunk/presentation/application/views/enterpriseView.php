        <h1 style="color: #FFF">Empresa</h1> &nbsp;&nbsp;&nbsp;
         <p style="color: #FFF">Nos dias que correm não existe uma entidade de estudo, mediação e gestão de resíduos industriais, para reaproveitamento destes. Existem já algumas empresas que realizam transformações específicas, como é o caso dos produtores de biodiesel. Na maior parte das vezes, o produto das transformações é utilizado intra-empresa, sem lucros directos sobre o produto final.

                               
        </p>
        <p style="color: #FFF">
            O objectivo da Re.INOVa será criar a base de conhecimento do mercado de resíduos necessária, para que haja uma sistematização e globalização do reaproveitamento de resíduos, e, numa fase mais avançada, gerir o reaproveitamento destes resíduos, através de contractos e parcerias entre possíveis produtores de resíduos, transformadores destes em matérias-primas, e utilizadores destas.
        </p>
        
         <p style="color: #FFF">
            A Re.Inova valoriza e incentiva a criação de parcerias estratégicas com empresas privadas, públicas, e universidades, de forma a conseguir com que estas entidades, bem como outras não referidas anteriormente, possam participar de forma mais activa no processo de transformação de resíduos. Este mercado oferece novas hipóteses de negócio a entidades que já possuem material técnico e científico adequado, mas que nunca antes ponderaram em utilizá-lo de uma forma rentável e ambiental neste tipo de processos de transformação.
            Pretendemos ser o elemento unificador entre as referidas entidades, pois só assim poderemos contribuir de forma eficiente para a redução do impacto resultante da problemática dos resíduos.
        </p>
        
